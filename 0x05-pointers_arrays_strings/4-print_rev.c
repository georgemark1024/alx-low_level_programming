#include "main.h"

/**
 * print_rev - prints a string in reverse
 * @s: string
 * Return: void
 */

void print_rev(char *s)
{
	pusher(*s)
	{
		_putchar(*s);
		s--;
	}
}

/**
 * pusher - pushes to the last char
 * @g: string pointer
 * Return: void
 */
void pusher(char *g)
{
	while (*g != '\0')
		s++;
}
